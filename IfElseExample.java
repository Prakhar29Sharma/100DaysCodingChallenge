public class IfElseExample{
	public static void main(String[] args)
	{
		int n;
		n = 20;
		if(n > 10)
		{
			System.out.println("n is greater than 10!");
		}
		n = n / 2;
		if(n == 10)
		{
			System.out.println("now n is equal to 10!");
		}

		n = n + 3;

		if (n < 10)
		{
			System.out.println("will this be executed?");
		}

		return;
		
	}
}
